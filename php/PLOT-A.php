<script>
  // Search Box Functionality
  const searchBox = document.getElementById('searchBox');
  searchBox.addEventListener('input', filterItems);

  function filterItems() {
    const searchTerm = searchBox.value.toLowerCase();
    const items = document.getElementsByClassName('grid-item');

    Array.from(items).forEach((item) => {
      const linkText = item.getElementsByTagName('a')[0].textContent.toLowerCase();  // Grave_ID
      const nameText = item.getElementsByTagName('p')[0].textContent.toLowerCase();  // Name_Deceased
      
      if (linkText.includes(searchTerm) || nameText.includes(searchTerm)) {
        item.style.display = 'block';
      } else {
        item.style.display = 'none';
      }
    });
  }
</script>

